// concurrently means that we are working on something at the same time but it's not 
// happening at the exact same unit of time. It's very confusing because it sounds similar
// to parallelism but running in parallel means literally at the exact same unit of time.
// You are doing something concurrently pretty much means you are switching between 
// different tasks and you are working on all of them but you are working on one at a time.

// So, within one CPU core, we have concurrent operation and within multiple CPU cores,
// we have parallel operations where we work on multiple threads at the exact same time.

