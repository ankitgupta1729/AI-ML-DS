SET DEFINE OFF;

CREATE OR REPLACE TRIGGER TRG_AUDIT_CLIENT
  AFTER INSERT OR UPDATE OR DELETE ON CLIENT_RECORD
  FOR EACH ROW
DECLARE
  v_op  VARCHAR2(10);
  v_key VARCHAR2(120);
BEGIN
  IF INSERTING THEN v_op := 'INSERT'; v_key := :NEW.PAN;
  ELSIF UPDATING THEN v_op := 'UPDATE'; v_key := :NEW.PAN;
  ELSE v_op := 'DELETE'; v_key := :OLD.PAN;
  END IF;
  INSERT INTO AUDIT_LOG (TABLE_NAME, OPERATION, KEY_VALUE, DB_USER)
  VALUES ('CLIENT_RECORD', v_op, v_key, USER);
END;
/

CREATE OR REPLACE TRIGGER TRG_AUDIT_ITR
  AFTER INSERT OR UPDATE OR DELETE ON INCOME_TAX_RECORD
  FOR EACH ROW
DECLARE
  v_op  VARCHAR2(10);
  v_key VARCHAR2(120);
BEGIN
  IF DELETING THEN
    v_op := 'DELETE';
    v_key := :OLD.PAN || '|' || TO_CHAR(:OLD.ASSES_YEAR_1,'YYYY-MM-DD') || '|R' || :OLD.RETURN_ORIGINAL_REVISED;
  ELSE
    v_op := CASE WHEN INSERTING THEN 'INSERT' ELSE 'UPDATE' END;
    v_key := :NEW.PAN || '|' || TO_CHAR(:NEW.ASSES_YEAR_1,'YYYY-MM-DD') || '|R' || :NEW.RETURN_ORIGINAL_REVISED;
  END IF;
  INSERT INTO AUDIT_LOG (TABLE_NAME, OPERATION, KEY_VALUE, DB_USER)
  VALUES ('INCOME_TAX_RECORD', v_op, v_key, USER);
END;
/

-- Generic audit for the three firm-account tables via one procedure pattern.
CREATE OR REPLACE TRIGGER TRG_AUDIT_TRADING
  AFTER INSERT OR UPDATE OR DELETE ON TRADING_ACCOUNT FOR EACH ROW
DECLARE v_op VARCHAR2(10); v_key VARCHAR2(120);
BEGIN
  IF DELETING THEN v_op:='DELETE'; v_key:=:OLD.PAN||'|'||TO_CHAR(:OLD.ASSES_YEAR_1,'YYYY-MM-DD');
  ELSE v_op:=CASE WHEN INSERTING THEN 'INSERT' ELSE 'UPDATE' END; v_key:=:NEW.PAN||'|'||TO_CHAR(:NEW.ASSES_YEAR_1,'YYYY-MM-DD'); END IF;
  INSERT INTO AUDIT_LOG (TABLE_NAME,OPERATION,KEY_VALUE,DB_USER) VALUES ('TRADING_ACCOUNT',v_op,v_key,USER);
END;
/

