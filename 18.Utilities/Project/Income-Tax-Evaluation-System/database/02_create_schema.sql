SET DEFINE OFF;

-- Drop in dependency order so the script is re-runnable.
BEGIN
  FOR t IN (SELECT table_name FROM user_tables
            WHERE table_name IN ('AUDIT_LOG','BALANCE_SHEET','PL_ACCOUNT','TRADING_ACCOUNT',
                                 'INCOME_TAX_RECORD','CLIENT_RECORD','APP_USER')) LOOP
    EXECUTE IMMEDIATE 'DROP TABLE ' || t.table_name || ' CASCADE CONSTRAINTS';
  END LOOP;
END;
/

-- ---------------------------------------------------------------------------
-- APP_USER — application login accounts (security mechanism, SRS §15)
-- Passwords are stored as SHA-256 hashes, never in clear text.
-- ---------------------------------------------------------------------------
CREATE TABLE APP_USER (
  USERNAME       VARCHAR2(30)  CONSTRAINT PK_APP_USER PRIMARY KEY,
  PASSWORD_HASH  VARCHAR2(64)  NOT NULL,
  FULL_NAME      VARCHAR2(60)  NOT NULL,
  ROLE           VARCHAR2(20)  DEFAULT 'Operator' NOT NULL,
  IS_ACTIVE      CHAR(1)       DEFAULT 'Y' NOT NULL CONSTRAINT CK_USER_ACTIVE CHECK (IS_ACTIVE IN ('Y','N')),
  CREATED_AT     DATE          DEFAULT SYSDATE NOT NULL
);

-- ---------------------------------------------------------------------------
-- CLIENT_RECORD — Client Information module (master)
-- ---------------------------------------------------------------------------
CREATE TABLE CLIENT_RECORD (
  PAN                        VARCHAR2(10) CONSTRAINT PK_CLIENT_RECORD PRIMARY KEY,
  CLIENT_NAME                VARCHAR2(30) NOT NULL,
  FATHERS_NAME               VARCHAR2(30),
  DOB                        DATE,
  PINCODE                    VARCHAR2(6),
  ADDRESS                    VARCHAR2(60),
  TELEPHONE                  VARCHAR2(15),
  SEX                        NUMBER(1)  DEFAULT 1 CONSTRAINT CK_SEX CHECK (SEX IN (0,1)),
  INDV_HUF_FIRM_AOP_LA       NUMBER(1)  DEFAULT 0 CONSTRAINT CK_CATEGORY CHECK (INDV_HUF_FIRM_AOP_LA BETWEEN 0 AND 4),
  RESIDENT_NR_NOR            NUMBER(1)  DEFAULT 0 CONSTRAINT CK_RESIDENCE CHECK (RESIDENT_NR_NOR BETWEEN 0 AND 2),
  WARD_CIRCLE_SPECIAL_RANGE  VARCHAR2(20),
  CONSTRAINT CK_PAN_FORMAT CHECK (REGEXP_LIKE(PAN, '^[A-Z]{5}[0-9]{4}[A-Z]$'))
);

