CREATE TABLE APP_USERS (
    USER_ID VARCHAR2(50) PRIMARY KEY,
    PASSWORD_HASH VARCHAR2(255) NOT NULL,
    FULL_NAME VARCHAR2(100),
    ROLE VARCHAR2(20) DEFAULT 'USER',
    CREATED_DATE DATE DEFAULT SYSDATE,
    IS_ACTIVE VARCHAR2(1) DEFAULT 'Y'
);

CREATE TABLE CLIENT_RECORD (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CLIENT_CODE VARCHAR2(20) UNIQUE NOT NULL,
    FULL_NAME VARCHAR2(100) NOT NULL,
    PAN VARCHAR2(10) NOT NULL,
    DATE_OF_BIRTH DATE,
    GENDER VARCHAR2(10),
    ADDRESS VARCHAR2(500),
    CITY VARCHAR2(50),
    STATE VARCHAR2(50),
    PINCODE VARCHAR2(6),
    TELEPHONE VARCHAR2(15),
    MOBILE VARCHAR2(15),
    EMAIL VARCHAR2(100),
    OCCUPATION VARCHAR2(50),
    AADHAR_NUMBER VARCHAR2(12),
    WARD_CIRCLE_SPECIAL_RANGE VARCHAR2(100),
    ASSESSMENT_YEAR VARCHAR2(9),
    CREATED_DATE DATE DEFAULT SYSDATE,
    UPDATED_DATE DATE DEFAULT SYSDATE,
    CREATED_BY VARCHAR2(50),
    UPDATED_BY VARCHAR2(50),
    CONSTRAINT PK_CLIENT_RECORD PRIMARY KEY (ID),
    CONSTRAINT CHK_CLIENT_GENDER CHECK (GENDER IN ('Male','Female','Other')),
    CONSTRAINT CHK_CLIENT_PAN CHECK (REGEXP_LIKE(PAN, '^[A-Z]{5}[0-9]{4}[A-Z]$')),
    CONSTRAINT CHK_CLIENT_PINCODE CHECK (REGEXP_LIKE(PINCODE, '^[0-9]{6}$'))
);

