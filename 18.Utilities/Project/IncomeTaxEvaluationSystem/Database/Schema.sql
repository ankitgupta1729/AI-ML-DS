CREATE OR REPLACE TRIGGER TRG_AUDIT_CLIENT
BEFORE INSERT OR UPDATE ON CLIENT_RECORD
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        :NEW.CREATED_DATE := SYSDATE;
        :NEW.CREATED_BY := NVL(:NEW.CREATED_BY, USER);
    END IF;
    :NEW.UPDATED_DATE := SYSDATE;
    :NEW.UPDATED_BY := NVL(:NEW.UPDATED_BY, USER);
END;
/

